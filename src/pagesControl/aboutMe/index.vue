        传统的汽车行业，核心工作就是聚焦产品，一切以产品为核心，通过对市场和用户的理解，研发出一款标准化的产品投入生产后，再去找各种营销方法和销售渠道去实现销售转化。这种千人一面的做法通过批量复制的效率提升，带动了汽车市场的快速增长；但是用户在满足了基本的“生存和保健需求”之后，近年来，凯翼汽车不断从传统的产品思维向用户思维转变，站在用户的角度来思考和解决问题。凯翼汽车用户思维包括三个层面，即通过大数据画像找到用户、通过用户共创研发出符合用户真正需要的产品、通过APP构建用户生态，在产品之外，为用户的生活带来更多可能。
