        爱车板块：一站式选车及个性化配置选搭、购车、控车、用车，实现人、车、生活的智慧连接。
        生活板块：建立三大用户体系，多样化的活动形式，KAIYI Life商城，全方面满足用户的精神需求与物质需求。
        凯翼汽车APP既能通过用户个性化的前装及后装配置选搭，实现用户千车千面的专属风格，有能通过构建围绕人、车、生活的生态构建，在汽车产品之外，为用户生活带来更多的可能。
